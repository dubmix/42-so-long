#include "../so_long.h"

int	ft_count_char(char *str, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (!str)
		return (-1);
	while (str && str[i])
	{
		if (str[i] == c)
			j++;
		i++;
	}
	return (j);
}
